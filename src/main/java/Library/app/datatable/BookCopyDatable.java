package Library.app.datatable;

public class BookCopyDatable {
	private String isbn;
	private String bookTitle;
	private String copyNumber;
	private String libraryMember;
	private String dueDate;
	private String status;
	private String isDueStatus;
	
	public BookCopyDatable() {
		
	}

	public String getIsbn() {
		return isbn;
	}

	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}

	public String getBookTitle() {
		return bookTitle;
	}

	public void setBookTitle(String bookTitle) {
		this.bookTitle = bookTitle;
	}

	public String getCopyNumber() {
		return copyNumber;
	}

	public void setCopyNumber(String copyNumber) {
		this.copyNumber = copyNumber;
	}

	public String getLibraryMember() {
		return libraryMember;
	}

	public void setLibraryMember(String libraryMember) {
		this.libraryMember = libraryMember;
	}

	public String getDueDate() {
		return dueDate;
	}

	public void setDueDate(String dueDate) {
		this.dueDate = dueDate;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getIsDueStatus() {
		return isDueStatus;
	}

	public void setIsDueStatus(String isDueStatus) {
		this.isDueStatus = isDueStatus;
	}
	
	
	
	
	

}
